use super::super::rank;

#[derive(Debug, Clone)]
pub struct Ascii {
  score: i64,
}

impl Ascii {
  pub fn new(score: i64) -> Ascii {
    Ascii { score }
  }

  pub fn rank_to_ascii(&mut self, rank: rank::rank::BenchMarkRank) -> String {
    match rank {
      rank::rank::BenchMarkRank::Lowest(_) => {
        format!("{}", LOWEST.to_string())
      }
      rank::rank::BenchMarkRank::Low(_) => {
        format!("{}", LOW.to_string())
      }
      rank::rank::BenchMarkRank::Medium(_) => {
        format!("{}", MEDIUM.to_string())
      }
      rank::rank::BenchMarkRank::High(_) => {
        format!("{}", HIGH.to_string())
      }
      rank::rank::BenchMarkRank::Highest(_) => {
        format!("{}", HIGHEST.to_string())
      }
      rank::rank::BenchMarkRank::Rage(_) => {
        format!("{}", RAGE.to_string())
      }
    }
  }

  pub fn bench_score_to_rank_enum(&mut self) -> rank::rank::BenchMarkRank {
    match self.score {
      0..=1000 => rank::rank::BenchMarkRank::Lowest("sad dick man...".to_string()),
      1001..=2000 => rank::rank::BenchMarkRank::Low("ok dick".to_string()),
      2001..=3000 => rank::rank::BenchMarkRank::Medium("noice dick!".to_string()),
      3001..=4000 => rank::rank::BenchMarkRank::High("good dick! wow!".to_string()),
      4001..=5000 => rank::rank::BenchMarkRank::Highest("awesome dick! please give me".to_string()),
      _ => rank::rank::BenchMarkRank::Rage("rage".to_string()),
    }
  }
}

